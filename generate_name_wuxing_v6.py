# 清理：去掉空set和全重复字符
for k in list(WUXING_CHARS.keys()):
    chars = set(WUXING_CHARS[k])
    # 去掉单字和空
    chars = {c for c in chars if len(c) == 1}
    WUXING_CHARS[k] = chars

# ── 3. 偏旁推断函数 ─────────────────────────────────────────────────
def get_radical(char):
    """取汉字部首"""
    import unicodedata
    for i in range(len(char)-1, -1, -1):
        cat = unicodedata.name(char[i], "")
        if "RADICAL" in cat:
            return char[i]
    return char[0] if char else ""

def infer_wuxing(char):
    """推断单字五行（偏旁法）"""
    rad = get_radical(char)
    if rad in RADICAL_WUXING:
        return RADICAL_WUXING[rad]
    # 兜底：按笔画数判（1,2木 3,4火 5,6土 7,8金 9,10水）
    strokes = len(char)
    if strokes <= 2:
        return "木"
    elif strokes <= 4:
        return "火"
    elif strokes <= 6:
        return "土"
    elif strokes <= 8:
        return "金"
    else:
        return "水"

# ── 4. GB2312常用字（3755字）────────────────────────────────────────
GB2312_CHARS = set()
# 常用汉字3500字（手写，太大；改用Unicode区段估计）
for cp in range(0x4E00, 0x9FFF + 1):
    try:
        c = chr(cp)
        GB2312_CHARS.add(c)
    except:
        pass

# ── 5. 生成结果 ─────────────────────────────────────────────────────
result = {}  # char -> wuxing

# 第一层：字库直接命中
for wuxing, chars in WUXING_CHARS.items():
    for c in chars:
        if c not in result:
            result[c] = wuxing

# 第二层：偏旁推断
for c in GB2312_CHARS:
    if c not in result and len(c) == 1:
        result[c] = infer_wuxing(c)

# ── 6. 输出SQL ─────────────────────────────────────────────────────
