#  构建
# ═══════════════════════════════════════════════════════════════
def build():
    result = {}
    for c in METAL:
        if is_cjk(c) and c not in result: result[c] = "金"
    for c in WOOD:
        if is_cjk(c) and c not in result: result[c] = "木"
    for c in WATER:
        if is_cjk(c) and c not in result: result[c] = "水"
    for c in FIRE:
        if is_cjk(c) and c not in result: result[c] = "火"
    for c in EARTH:
        if is_cjk(c) and c not in result: result[c] = "土"
    return result

