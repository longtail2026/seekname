// ──────────────────────────────────────────────
// 单一名字的硬性过滤
// ──────────────────────────────────────────────

/**
 * 对单个名字进行硬性过滤检查
 * @param givenName 名（不含姓）
 * @param pinyin 拼音（带声调）
 * @param options 过滤选项
 * @returns 所有触发的淘汰原因（为空则通过）
 */
export function hardFilterSingleName(
  givenName: string,
  pinyin?: string,
  options: HardFilterOptions = {}
): HardFilterResult[] {
  const results: HardFilterResult[] = [];

  // ── 规则1: 生僻字过滤 ──
  for (const char of givenName) {
    if (checkRareChar(char)) {
      results.push({
        passed: false,
        reason: `包含生僻字"${char}"，不在GB2312一级字库/现代常用字表内`,
        rule: "生僻字过滤",
      });
      break;
    }
  }

  // ── 规则2: 负面含义过滤 ──
  for (const char of givenName) {
    if (NEGATIVE_SINGLE_CHARS.has(char)) {
      results.push({
        passed: false,
        reason: `包含负面含义字"${char}"（敏感字库匹配）`,
        rule: "负面含义",
      });
      break;
    }
  }

  for (const word of NEGATIVE_TWO_CHAR_WORDS) {
    if (givenName.includes(word)) {
      results.push({
        passed: false,
        reason: `名字包含负面含义词"${word}"`,
        rule: "负面含义",
      });
      break;
    }
  }

  // ── 规则3: 谐音避讳（包含姓氏拼音拼接检测） ──
  if (pinyin) {
    const surnamePinyin = options.surname ? getSurnamePinyin(options.surname) : undefined;
    const homophoneResult = checkHomophoneTaboo(pinyin, givenName, surnamePinyin);
    if (homophoneResult.isTaboo) {
      results.push({
        passed: false,
        reason: `谐音忌讳：名字拼音"${pinyin}"${homophoneResult.description}（${homophoneResult.tabooWord}）`,
        rule: "谐音避讳",
      });
    }
  }

  // ── 规则4: 重名限制（可选，检查完整姓名：姓氏+名） ──
  if (options.checkFamousNames && options.surname) {
    const fullName = options.surname + givenName;
    if (FAMOUS_NAMES.has(fullName)) {
      results.push({
        passed: false,
        reason: `与知名人物"${fullName}"重名`,
        rule: "重名限制",
      });
    }
  }

  // ── 规则5: 笔画极端 ──
  // 只计算【名字（givenName）】的总笔画，不含姓氏
  // 姓氏由客户固定不可更改，不应计入笔画平衡淘汰
  const minStrokes = options.minStrokes ?? 8;
  const maxStrokes = options.maxStrokes ?? 35;
  const totalStrokes = calculateTotalStrokes(givenName);

  if (totalStrokes < minStrokes) {
    results.push({
      passed: false,
      reason: `名字笔画过少：${totalStrokes}画（<${minStrokes}画）`,
      rule: "笔画极端",
    });
  } else if (totalStrokes > maxStrokes) {
    results.push({
      passed: false,
      reason: `名字笔画过多：${totalStrokes}画（>${maxStrokes}画）`,
      rule: "笔画极端",
    });
  }

  return results;
}

// ──────────────────────────────────────────────
// 名字列表的硬性过滤（主入口）
// ──────────────────────────────────────────────

/**
 * 对候选名字列表进行硬性过滤（一票否决制）
 * 任何一项规则不通过，直接淘汰
 *
 * @param names 候选名字列表（需有 name/givenName 和可选的 pinyin 字段）
 * @param options 过滤配置
 * @returns 通过和淘汰的名字
 */
export function hardFilterNames<T extends { name: string; givenName?: string; pinyin?: string }>(
  names: T[],
  options: HardFilterOptions = {}
): HardFilterNamesResult<T> {
  const passed: T[] = [];
  const removed: HardFilterNamesResult<T>["removed"] = [];

  for (const name of names) {
    let givenName = name.givenName || "";
    if (!givenName && name.name) {
      givenName = name.name;
      if (options.surname && name.name.startsWith(options.surname)) {
        givenName = name.name.slice(options.surname.length);
      }
    }

    if (!givenName) {
      passed.push(name);
      continue;
    }

    const reasons = hardFilterSingleName(givenName, name.pinyin, options);

    if (reasons.length === 0) {
      passed.push(name);
    } else {
      removed.push({
        name: name.name || givenName,
        reasons,
      });
    }
  }

  return { passed, removed };
}

/**
 * 获取硬性过滤的淘汰原因摘要（用于日志/调试）
 */
export function summarizeRemoved(removed: HardFilterNamesResult<any>["removed"]): string {
  return removed
    .map(r => {
      const reasonStrs = r.reasons.map(rev => `[${rev.rule}]${rev.reason}`).join("; ");
      return `  ✗ ${r.name}: ${reasonStrs}`;
    })
    .join("\n");
}

