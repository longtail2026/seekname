const STROKE_MAP = buildStrokeMap();

/**
 * 获取汉字笔画数
 * 未收录的汉字使用启发式估算
 */
function getStrokeCount(char: string): number {
  if (STROKE_MAP.has(char)) {
    return STROKE_MAP.get(char)!;
  }

  const code = char.charCodeAt(0);
  if (code >= 0x4E00 && code <= 0x9FFF) {
    const estimated = Math.round(8 + ((code - 0x4E00) / (0x9FFF - 0x4E00)) * 20);
    return Math.min(Math.max(estimated, 2), 35);
  }

  return 5;
}

/**
 * 计算名字（不含姓）的总笔画数
 */
function calculateTotalStrokes(givenName: string): number {
  let total = 0;
  for (const char of givenName) {
    total += getStrokeCount(char);
  }
  return total;
}

