/**
 * 检查名字是否含有忌用字
 * @param char 要检查的字
 * @param gender 性别 "M" | "F"
 * @returns 忌用原因字符串，如果可用则返回 null
 */
export function checkTabooChar(char: string, gender?: "M" | "F"): string | null {
  // 先检查男女皆忌
  if (UNIVERSAL_TABOO_CHARS.has(char)) {
    return "此字为通用忌用字，不宜用于姓名";
  }
  // 再按性别检查
  if (gender === "F" && FEMALE_TABOO_CHARS.has(char)) {
    return "此字为女性忌用字，不利婚姻情感";
  }
  if (gender === "M" && MALE_TABOO_CHARS.has(char)) {
    return "此字为男性忌用字，对事业运势有影响";
  }
  return null;
}

