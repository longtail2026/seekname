// 停用词（提取关键词时忽略这些）
const STOP_WORDS = new Set([
  "的", "得", "地", "了", "是", "在", "有", "和", "与", "或",
  "请", "给", "帮", "我", "你", "他", "她", "它", "们",
  "宝宝", "孩子", "希望", "想要", "希望", "要", "请",
  "一个", "一点", "一些", "比较", "特别", "非常",
  "一点", "比较", "稍微", "略", "很", "太", "更",
  "起名", "取名", "名字", "名", "叫", "取",
  "男", "女", "男孩", "女孩",
]);

// 标点符号
const PUNCT_PATTERN = /[,，.。!！?？;；:：""''【】\[\]()（）『』《》<>]/g;

/**
 * 从自然语言文本中提取关键词
 * - 移除停用词和标点
 * - 识别 2-4 字词
 * - 过滤纯数字/字母
 */
function extractKeywords(text: string): string[] {
  const cleaned = text.replace(PUNCT_PATTERN, " ").trim();
  const words: string[] = [];

  // 按空格分词
  const parts = cleaned.split(/\s+/).filter(Boolean);

  for (const part of parts) {
    // 识别连续汉字词（2-4字）
    const chineseOnly = part.match(/[\u4e00-\u9fa5]{2,4}/g) || [];
    words.push(...chineseOnly);
  }

  // 过滤停用词
  const filtered = words.filter((w) => !STOP_WORDS.has(w));

  // 去重
  return [...new Set<string>(filtered)];
}

/**
 * 扩展关键词为同义词列表
 * 策略：精确匹配 > 部分包含匹配
 */
function expandWithSynonyms(keywords: string[]): string[] {
  const expanded = new Set<string>(keywords); // 原始词也保留

  for (const keyword of keywords) {
    // 1. 精确匹配
    if (INTENT_SYNONYMS[keyword]) {
      INTENT_SYNONYMS[keyword].forEach((s) => expanded.add(s));
    }

    // 2. 部分包含匹配（如"聪明伶俐" 包含 "聪明" 和 "伶俐"）
    for (const [key, synonyms] of Object.entries(INTENT_SYNONYMS)) {
      if (keyword.includes(key) || key.includes(keyword)) {
        synonyms.forEach((s) => expanded.add(s));
        expanded.add(key); // 把 key 本身也加入
      }
    }
  }

  return [...expanded];
}

/**
 * 主函数：解析用户输入 → 返回扩展后的关键词列表
 *
 * @param input 用户输入，如 "希望宝宝聪明伶俐，性格温柔"
 * @returns { keywords: 原始关键词, expanded: 扩展后关键词 }
 */
export function parseIntent(input?: string | null): {
  keywords: string[];
  expanded: string[];
} {
  if (!input || input.trim().length < 2) {
    // 默认返回积极品质关键词
    return {
      keywords: ["德", "才", "智", "仁", "义"],
      expanded: ["德", "才", "智", "仁", "义"],
    };
  }

  const keywords = extractKeywords(input);
  if (!keywords.length) {
    return {
      keywords: [],
      expanded: ["德", "才", "智", "仁", "义"],
    };
  }

  const expanded = expandWithSynonyms(keywords);

  return { keywords, expanded };
}
