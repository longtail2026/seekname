// 停用词（提取关键词时忽略这些）
const STOP_WORDS = new Set([
  "的", "得", "地", "了", "是", "在", "有", "和", "与", "或",
  "请", "给", "帮", "我", "你", "他", "她", "它", "们",
  "宝宝", "孩子", "希望", "想要", "希望", "要", "请",
  "一个", "一点", "一些", "比较", "特别", "非常",
  "一点", "比较", "稍微", "略", "很", "太", "更",
  "起名", "取名", "名字", "名", "叫", "取",
  "男", "女", "男孩", "女孩",
]);

// 标点符号
const PUNCT_PATTERN = /[,，.。!！?？;；:：""''【】\[\]()（）『』《》<>]/g;

/**
 * 从自然语言文本中提取关键词
 * - 移除停用词和标点
 * - 识别 2-4 字词
 * - 过滤纯数字/字母
 */
function extractKeywords(text: string): string[] {
  const cleaned = text.replace(PUNCT_PATTERN, " ").trim();
  const words: string[] = [];

  // 按空格分词
  const parts = cleaned.split(/\s+/).filter(Boolean);

  for (const part of parts) {
    // 识别连续汉字词（2-4字）
    const chineseOnly = part.match(/[\u4e00-\u9fa5]{2,4}/g) || [];
    words.push(...chineseOnly);
  }

  // 过滤停用词
  const filtered = words.filter((w) => !STOP_WORDS.has(w));

  // 去重
  return [...new Set<string>(filtered)];
}

/**
 * 扩展关键词为同义词列表
 * 策略：
 * 1. 精确匹配优先（保留完整意图词如"才华"）
 * 2. 扩展时：完整词优先，单字符作为后备补充（不干扰典籍查询优先级）
 * 3. 返回带优先级的关键词列表：完整词在前，单字符在后
 */
function expandWithSynonyms(keywords: string[]): { priority: string[]; fallback: string[] } {
  const priority = new Set<string>();   // 完整词（2-4字），优先用于典籍查询
  const fallback = new Set<string>();    // 单字符，作为后备

  for (const keyword of keywords) {
    // 单字符直接加入 fallback
    if (keyword.length === 1) {
      fallback.add(keyword);
      continue;
    }

    // 1. 精确匹配（如"才华"）
    if (INTENT_SYNONYMS[keyword]) {
      priority.add(keyword); // 保留完整词到优先级列表
      // 同义词也加入 priority（它们通常是多字词）
      INTENT_SYNONYMS[keyword].forEach((s) => {
        if (s.length >= 2) priority.add(s);
        else fallback.add(s);
      });
      continue;
    }

    // 2. 精确匹配失败后，尝试匹配更长的已知意图词
    //    如"有才华" 包含 "才华" → 触发"才华"映射
    let matchedLonger = false;
    for (const [key, synonyms] of Object.entries(INTENT_SYNONYMS)) {
      if (key.length >= 2 && keyword.includes(key)) {
        priority.add(key);
        synonyms.forEach((s) => {
          if (s.length >= 2) priority.add(s);
          else fallback.add(s);
        });
        matchedLonger = true;
      }
    }
    if (matchedLonger) continue;

    // 3. 没有精确匹配，才按字符拆解（单字）
    for (const char of keyword) {
      if (/[\u4e00-\u9fa5]/.test(char)) {
        fallback.add(char);
      }
    }
  }

  return {
    priority: [...priority],
    fallback: [...fallback],
  };
}

/**
 * 主函数：解析用户输入 → 返回扩展后的关键词列表
 * 返回优先级分离的结果：priority（完整词）用于优先查询典籍，fallback（单字符）作为后备
 *
 * @param input 用户输入，如 "希望宝宝聪明伶俐，性格温柔"
 * @returns { keywords: 原始关键词, expanded: 扩展后关键词, priority: 优先查询的关键词, fallback: 后备关键词 }
 */
export function parseIntent(input?: string | null): {
  keywords: string[];
  expanded: string[];
  priority: string[];
  fallback: string[];
} {
  if (!input || input.trim().length < 2) {
    // 默认返回积极品质关键词
    return {
      keywords: ["德", "才", "智", "仁", "义"],
      expanded: ["德", "才", "智", "仁", "义"],
      priority: ["德", "才", "智", "仁", "义"],
      fallback: [],
    };
  }

  const keywords = extractKeywords(input);
  if (!keywords.length) {
    return {
      keywords: [],
      expanded: ["德", "才", "智", "仁", "义"],
      priority: ["德", "才", "智", "仁", "义"],
      fallback: [],
    };
  }

  const { priority, fallback } = expandWithSynonyms(keywords);
  // expanded 仍然返回全部（兼容旧代码）
  const expanded = [...priority, ...fallback];

  return { keywords, expanded, priority, fallback };
}
